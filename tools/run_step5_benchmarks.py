"""Run Step 5 benchmark set and persist reproducible artifacts."""

from __future__ import annotations

from datetime import date
import hashlib
import json
from pathlib import Path
import sys

REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_ROOT = REPO_ROOT / "src"
if str(SRC_ROOT) not in sys.path:
    sys.path.insert(0, str(SRC_ROOT))

from ccs.output.report import write_html_report, write_json_report
from ccs.simulation.engine import run_strategy_comparison
from ccs.simulation.scenarios import load_scenario


def main() -> int:
    scenario_root = REPO_ROOT / "benchmarks" / "scenarios"
    output_root = REPO_ROOT / "benchmarks" / "results" / "step5"
    output_root.mkdir(parents=True, exist_ok=True)

    scenario_files = [
        "read_heavy.yaml",
        "write_heavy.yaml",
        "parallel_editing.yaml",
        "large_artifact_reasoning.yaml",
        "access_always_read.yaml",
        "access_conditional_injection.yaml",
        "access_pointer.yaml",
    ]

    runs = 10
    strategies = ["eager", "lazy"]
    manifest: dict[str, object] = {
        "generated_on": str(date.today()),
        "runs_per_strategy": runs,
        "strategies": strategies,
        "scenarios": [],
    }

    summary_rows: list[str] = []
    summary_rows.append(
        "| scenario | context_model | eager_sync_tokens | lazy_sync_tokens | lazy_savings_vs_eager |"
    )
    summary_rows.append("|---|---:|---:|---:|---:|")

    for file_name in scenario_files:
        scenario_path = scenario_root / file_name
        scenario = load_scenario(str(scenario_path))
        seed_start = int(scenario["simulation"]["seed"])
        report = run_strategy_comparison(
            scenario,
            strategies=strategies,
            runs=runs,
            seed_start=seed_start,
        )

        stem = scenario_path.stem
        out_json = output_root / f"{stem}.json"
        out_html = output_root / f"{stem}.html"
        write_json_report(report, out_json)
        write_html_report(report, out_html)

        aggregated = {item["strategy"]: item for item in report.aggregated}
        eager_tokens = float(aggregated["eager"]["synchronization_tokens_mean"])
        lazy_tokens = float(aggregated["lazy"]["synchronization_tokens_mean"])
        savings = 0.0 if eager_tokens <= 0 else max(0.0, 1.0 - (lazy_tokens / eager_tokens))
        context_model = scenario["context_semantics"]["model"]
        summary_rows.append(
            f"| {scenario['scenario']['name']} | {context_model} | "
            f"{eager_tokens:.2f} | {lazy_tokens:.2f} | {savings:.4f} |"
        )

        manifest["scenarios"].append(
            {
                "scenario_file": str(scenario_path.relative_to(REPO_ROOT)),
                "scenario_name": scenario["scenario"]["name"],
                "context_model": context_model,
                "seed_start": seed_start,
                "runs_per_strategy": runs,
                "strategies": strategies,
                "output_json": str(out_json.relative_to(REPO_ROOT)),
                "output_html": str(out_html.relative_to(REPO_ROOT)),
            }
        )

    summary_path = output_root / "SUMMARY.md"
    summary_contents = (
        "\n".join(
            [
                "# Step 5 Benchmark Summary",
                "",
                "This file is generated by `tools/run_step5_benchmarks.py`.",
                "",
                f"- generated_on: {manifest['generated_on']}",
                f"- runs_per_strategy: {runs}",
                f"- strategies: {', '.join(strategies)}",
                "",
                *summary_rows,
                "",
                "See `manifest.json` for full reproducibility metadata (scenario files, seeds, outputs).",
            ]
        )
        + "\n"
    )
    summary_path.write_text(summary_contents, encoding="utf-8")

    manifest["baseline_checksums"] = {
        "SUMMARY.md": f"sha256:{hashlib.sha256(summary_contents.encode('utf-8')).hexdigest()}"
    }
    manifest_path = output_root / "manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2, sort_keys=True), encoding="utf-8")
    print(f"Wrote benchmark artifacts to {output_root}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
